return function()
  require("modules.utf8")

  describe("utf8", function()
    describe("utf8len", function()
      it("returns the correct length", function()
        assert_equal(0, string.utf8len(""))
        assert_equal(3, string.utf8len("abc"))
        assert_equal(3, string.utf8len("친칛칩"))
        assert_equal(11, string.utf8len("foo 친칛칩 bar"))
        assert_equal(1, string.utf8len("游"))
      end)
    end)

    describe("utf8upper", function()
      it("returns the uppercase string", function()
        assert_equal("", string.utf8upper(""))
        assert_equal("ABC", string.utf8upper("abc"))
        assert_equal("칀츿칍", string.utf8upper("친칛칩"))
        assert_equal("FOO 칀츿칍 BAR", string.utf8upper("foo 친칛칩 bar"))
        assert_equal("游", string.utf8upper("游"))
      end)
    end)

    describe("utf8lower", function()
      it("returns the lowercase string", function()
        assert_equal("", string.utf8lower(""))
        assert_equal("abc", string.utf8lower("ABC"))
        assert_equal("친칛칩", string.utf8lower("칀츿칍"))
        assert_equal("foo 친칛칩 bar", string.utf8lower("FOO 칀츿칍 BAR"))
        assert_equal("游", string.utf8lower("游"))
      end)
    end)
  end)
end
